tresc = "Python jest super"

# a) Zerowy indeks
print("Zerowy indeks:", tresc[0])

# b) Ostatni indeks
print("Ostatni indeks:", tresc[-1])

# c) Co drugi znak, zaczynając od zerowego
print("Co drugi znak, zaczynając od zerowego:", tresc[::2])

# d) Co trzeci znak, zaczynając od pierwszego
print("Co trzeci znak, zaczynając od pierwszego:", tresc[1::3])

# e) Od dziesiątego do ostatniego
print("Od dziesiątego do ostatniego:", tresc[10:])

# f) Od końca do początku
print("Od końca do początku:", tresc[::-1])
